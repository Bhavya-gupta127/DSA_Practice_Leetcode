class Solution
{
    public:
    //Function to get the maximum total value in the knapsack.
    double fractionalKnapsack(int W, Item arr[], int n)
    {
        vector<pair<double,int>>ratio;
        for(int i=0;i<n;i++)
            ratio.push_back({(double)arr[i].value/(double)arr[i].weight,arr[i].weight});
        sort(ratio.rbegin(),ratio.rend());
        int i=0;
        double ans=0;
        while(W>0 && i<n)
        {
            double temp=min(W,ratio[i].second);
            W-=temp;
            ans+=ratio[i].first*temp;
            i++;
        }
        return ans;
    }
        
};


